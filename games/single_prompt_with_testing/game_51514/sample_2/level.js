export function loadLevel() {
    gameState.platforms = [];
    gameState.blocks = [];
    gameState.collectibles = [];
    gameState.hazards = [];
    
    const map = refinedMap;
    gameState.levelH = map.length * TILE_SIZE;
    gameState.levelW = map[0].length * TILE_SIZE;

    for (let row = 0; row < map.length; row++) {
        for (let col = 0; col < map[row].length; col++) {
            const char = map[row][col];
            const x = col * TILE_SIZE;
            const y = row * TILE_SIZE;

            switch (char) {
                case '#':
                    gameState.platforms.push(new Platform(x, y, TILE_SIZE, TILE_SIZE));
                    break;
                case 'B':
                    gameState.blocks.push(new DestructibleBlock(x, y));
                    break;
                case 'P':
                    gameState.player = new Player(x, y);
                    break;
                case 'S':
                    gameState.hazards.push(new Hazard(x, y + TILE_SIZE/2, TILE_SIZE, TILE_SIZE/2));
                    break;
                case 'C':
                    gameState.collectibles.push(new Collectible(x + TILE_SIZE/4, y + TILE_SIZE/4));
                    break;
                case 'G':
                    gameState.goal = new Goal(x, y);
                    // Add a platform under goal if needed
                    gameState.platforms.push(new Platform(x, y + TILE_SIZE, TILE_SIZE, TILE_SIZE)); 
                    break;
            }
        }
    }
}