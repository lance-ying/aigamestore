export function getLevelData(levelIndex) {
  if (levelIndex < 0 || levelIndex >= LEVELS.length) {
    return null;
  }
  return LEVELS[levelIndex];
}

export function getTileAt(level, x, y) {
  const col = Math.floor(x / TILE_SIZE);
  const row = Math.floor(y / TILE_SIZE);
  
  if (row < 0 || row >= level.tiles.length || col < 0 || col >= level.tiles[0].length) {
    return 0;
  }
  
  return level.tiles[row][col];
}

export function isSolidTile(tile) {
  return tile === 1 || tile === 5;
}

export function isWallTile(tile) {
  return tile === 5;
}

export function isSpikeTile(tile) {
  return tile === 2;
}

export function isGoalTile(tile) {
  return tile === 3;
}

export function isStrawberryTile(tile) {
  return tile === 4;
}